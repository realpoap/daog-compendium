export const ItemTypeSchema = z.enum([
	'weapon',
	'armor',
	'shield',
	'garnment',
	'accessorie',
	'trinket',
	'jewell',
	'potion',
	'ammunition',
	'food',
	'junk',
	'book',
]);

export type ItemTypeType = `${z.infer<typeof ItemTypeSchema>}`;

export const ItemImportSchema = z.object({
	itemType: ItemTypeSchema,
	name: z.string(),
	weaponType: z.string(),
	material: z.string(),
	materialType: z.string(),
	materialSubType: z.string().nullable(),
	weight: z.number().nullable(),
	value: z.number().nullable(),
	protection: z.number().nullable(),
	durability: z.string().nullable(),
	damages: z.string().nullable(),
	isFood: z.boolean(),
});

export const ItemNewSchema = z.object({
	itemType: ItemTypeSchema,
	searchName: z.string(),
	weaponType: z.string(),
	name: z.string().array(),
	material: z.string(),
	materialType: z.string(),
	quality: z.string(),
	isFood: z.boolean(),
});

